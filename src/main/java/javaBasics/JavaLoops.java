package javaBasics;

public class JavaLoops {
	
	public static void main(String args[]) {
		
		for(int i=0;i<5;i++) {
			System.out.println("For loop");
		}
		
		int x = 0;
		while(x>5) {
			System.out.println("While loop");
			x++;
		}
		
		int y = 0;
		do {
			System.out.println("Do while loop");
		}while(y>5);
		
	}

}
